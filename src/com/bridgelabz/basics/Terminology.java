package com.bridgelabz.basics;


// JDK => Java Development Kit -> OS Specific installation
// JDK -> JRE + Compiler + Javadocs,debuggers
// JRE => Java Runtime Environment
// JRE -> JVM + browser plugins etc.
// JVM => Java Virtual Machine

// .java file(source code) is given to compiler(javac)
// Compiler will convert the source code(.java) into the byte code(.class file)
// This byte code is given to JVM(interpreter)
// The code is interpreted from byte code to machine readable code
// This machine readable code is finally executed by JVM.

public class Terminology {


    // main method is executed by main thread => JVM
    public static void main(String[] args) {
        System.out.println("Hello");
    }
}
